public class Curso {
    private int codCurso;
    private String nomeCuro;
    private int coordenadorCod;

    public int getCodCurso() {
        return codCurso;
    }

    public void setCodCurso(int codCurso) {
        this.codCurso = codCurso;
    }

    public String getNomeCuro() {
        return nomeCuro;
    }

    public void setNomeCuro(String nomeCuro) {
        this.nomeCuro = nomeCuro;
    }

    public int getCoordenadorCod() {
        return coordenadorCod;
    }

    public void setCoordenadorCod(int coordenadorCod) {
        this.coordenadorCod = coordenadorCod;
    }
    
}
